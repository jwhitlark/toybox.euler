"The series, 1^1 + 2^2 + 3^3 + ... + 10^10 = 10405071317.

Find the last ten digits of the series, 1^1 + 2^2 + 3^3 + ... + 1000^1000."

(defn solve []
  (time (->> (range 1 1001)
             (map #(clj-math/expt % %))
             (reduce +)
             (str)
             (take-last 10)
             (apply str))))

